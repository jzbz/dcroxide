/// The connection-manager adapter answering the RPC handlers' peer
/// queries over the daemon's live-peer registry (a growing slice of
/// the `RpcConnManager` seam).
pub struct NodeRpcConnManager {
    connected: crate::runtime::ConnectedPeers,
    net_totals: Arc<crate::transport::NetByteTotals>,
    /// The sync-manager peer registry, backing `getpeerinfo` (and the
    /// relay fan-out).  Held at the top level, not inside the relay
    /// option, so peer info is available even when the relay handles are
    /// not attached.
    sync_peers: crate::dispatch::SyncPeers,
    relay: Option<RpcRelaySinks>,
}

/// The relay handles the RPC connection manager drives when a
/// submitted transaction should announce to the network.
struct RpcRelaySinks {
    recently_advertised: Arc<Mutex<dcroxide_containers::lru::Map<Hash, dcroxide_wire::MsgTx>>>,
    tx_pool: Arc<Mutex<crate::txmempool::NodeTxPool>>,
    rebroadcast: crate::rebroadcast::RebroadcastSink,
    /// The websocket notification manager, so a submitted transaction
    /// reaches subscribed clients as well as peers (dcrd
    /// `handleSendRawTransaction` calling `NotifyNewTransactions`
    /// alongside `RelayTransactions`).
    ntfn: crate::websocket::NodeNtfnMgr,
}

impl NodeRpcConnManager {
    /// Adapt the connected-peer registry and byte totals for the RPC
    /// handlers.
    pub fn new(
        connected: crate::runtime::ConnectedPeers,
        net_totals: Arc<crate::transport::NetByteTotals>,
    ) -> NodeRpcConnManager {
        NodeRpcConnManager {
            connected,
            net_totals,
            sync_peers: crate::dispatch::SyncPeers::default(),
            relay: None,
        }
    }

    /// Attach only the sync-manager peer registry that backs
    /// `getpeerinfo`, without the transaction-relay handles.  The daemon
    /// always installs both together through
    /// [`with_relay`](Self::with_relay), so this exists solely to build
    /// the peer-info seam in isolation for tests.
    #[cfg(test)]
    fn with_peer_registry(mut self, sync_peers: crate::dispatch::SyncPeers) -> NodeRpcConnManager {
        self.sync_peers = sync_peers;
        self
    }

    /// Attach the sync-manager peer registry (backing `getpeerinfo`) and
    /// the relay handles so submitted transactions announce to the
    /// network (dcrd rpcConnManager's `RelayTransactions`).
    pub fn with_relay(
        mut self,
        sync_peers: crate::dispatch::SyncPeers,
        recently_advertised: Arc<Mutex<dcroxide_containers::lru::Map<Hash, dcroxide_wire::MsgTx>>>,
        tx_pool: Arc<Mutex<crate::txmempool::NodeTxPool>>,
        rebroadcast: crate::rebroadcast::RebroadcastSink,
        ntfn: crate::websocket::NodeNtfnMgr,
    ) -> NodeRpcConnManager {
        self.sync_peers = sync_peers;
        self.relay = Some(RpcRelaySinks {
            recently_advertised,
            tx_pool,
            rebroadcast,
            ntfn,
        });
        self
    }
}

impl dcroxide_rpc::server::RpcConnManager for NodeRpcConnManager {
    fn connected_count(&mut self) -> i32 {
        self.connected.len() as i32
    }

    /// Snapshot the live peers for `getpeerinfo` (dcrd
    /// `rpcConnManager.ConnectedPeers`); the handler sorts by id and
    /// flags the sync peer itself.
    fn connected_peers(&mut self) -> Vec<dcroxide_rpc::server::RpcPeerInfo> {
        self.sync_peers.connected_peer_infos()
    }

    /// The persistent peers for `getaddednodeinfo` (dcrd
    /// `rpcConnManager.PersistentPeers`).
    fn persistent_peers(&mut self) -> Vec<dcroxide_rpc::server::RpcAddedNode> {
        self.sync_peers.persistent_peers()
    }

    /// Disconnect the non-permanent peer with the given id (dcrd
    /// `rpcConnManager.DisconnectByID`); a permanent or absent peer is
    /// "peer not found", which the `node` handler turns into its "use
    /// remove" hint.
    fn disconnect_by_id(&mut self, id: i32) -> Result<(), String> {
        if self.sync_peers.disconnect_by_id(id) {
            Ok(())
        } else {
            Err("peer not found".to_string())
        }
    }

    /// Disconnect the non-permanent peers at the given address (dcrd
    /// `rpcConnManager.DisconnectByAddr`).
    fn disconnect_by_addr(&mut self, addr: &str) -> Result<(), String> {
        if self.sync_peers.disconnect_by_addr(addr) {
            Ok(())
        } else {
            Err("peer not found".to_string())
        }
    }

    /// Resolve a host to its addresses for `getaddednodeinfo` with DNS
    /// details (dcrd `rpcConnManager.Lookup` over the config's lookup
    /// function; the system resolver here — a `.onion`/proxied host
    /// resolves through it rather than dcrd's SOCKS lookup, a documented
    /// divergence until the Tor path is wired).
    fn lookup(&mut self, host: &str) -> Result<Vec<String>, String> {
        use std::net::ToSocketAddrs;
        (host, 0u16)
            .to_socket_addrs()
            .map(|addrs| addrs.map(|addr| addr.ip().to_string()).collect())
            .map_err(|e| e.to_string())
    }

    /// Announce submitted transactions to the network (dcrd
    /// `relayTransactions` from the RPC adaptor).
    fn relay_transactions(&mut self, tx_hashes: &[Hash]) {
        let Some(relay) = &self.relay else {
            return;
        };
        let mut notify_pairs: Vec<(dcroxide_wire::MsgTx, i8)> = Vec::new();
        for hash in tx_hashes {
            let fetched = {
                let pool = relay.tx_pool.lock().expect("tx pool mutex poisoned");
                pool.fetch_transaction(hash)
            };
            if let Some(tx) = fetched {
                let tree =
                    if dcroxide_stake::determine_tx_type(&tx) == dcroxide_stake::TxType::Regular {
                        dcroxide_wire::TX_TREE_REGULAR
                    } else {
                        dcroxide_wire::TX_TREE_STAKE
                    };
                relay
                    .recently_advertised
                    .lock()
                    .expect("recently advertised poisoned")
                    .put(*hash, tx.clone());
                notify_pairs.push((tx, tree));
            }
            self.sync_peers
                .relay_inventory(&crate::server::RelayInvFacts {
                    inv_type: dcroxide_wire::InvType::TX,
                    inv_hash: *hash,
                    req_services: dcroxide_wire::ServiceFlag(0),
                    immediate: false,
                    data_is_block_header: false,
                    data_is_tx: true,
                });
        }
        // Notify subscribed websocket clients of the accepted transactions,
        // mirroring dcrd's `handleSendRawTransaction` calling
        // `NotifyNewTransactions(acceptedTxs)` right after
        // `RelayTransactions`.  This seam's sole caller is that handler, so
        // bundling the notify here is observably equivalent.
        if !notify_pairs.is_empty() {
            relay.ntfn.notify_new_transactions(notify_pairs);
        }
    }

    fn add_rebroadcast_inventory(&mut self, tx_hash: &Hash, tx: &dcroxide_wire::MsgTx) {
        // Track user-submitted transactions for periodic rebroadcast
        // until they make it into a block (dcrd rpcConnManager
        // forwarding to the server's `AddRebroadcastInventory`).
        let Some(relay) = &self.relay else {
            return;
        };
        relay.rebroadcast.add_rebroadcast_inventory(tx_hash, tx);
    }

    fn net_totals(&mut self) -> (u64, u64) {
        // Received first, sent second (dcrd `NetTotals`).
        (
            self.net_totals
                .bytes_received
                .load(std::sync::atomic::Ordering::Relaxed),
            self.net_totals
                .bytes_sent
                .load(std::sync::atomic::Ordering::Relaxed),
        )
    }
}

