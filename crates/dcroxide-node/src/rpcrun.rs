    /// Poll `cond` until it holds or the timeout elapses.
    fn wait_until(timeout: std::time::Duration, mut cond: impl FnMut() -> bool) -> bool {
        let start = std::time::Instant::now();
        while start.elapsed() < timeout {
            if cond() {
                return true;
            }
            std::thread::sleep(std::time::Duration::from_millis(10));
        }
        cond()
    }

    /// The manual peer-control seams drive a live outbound driver
    /// (dcrd `rpcConnManager.Connect`/`RemoveByAddr`/`RemoveByID`):
    /// `addnode add` dials and refuses duplicates in dcrd's check
    /// order, and removing the persistent peer disconnects it without
    /// a redial despite its permanence.
    #[test]
    fn connect_and_remove_control_the_outbound_driver() {
        use std::net::{TcpListener, TcpStream};

        // Two live listeners the driver can dial (their kernel backlog
        // completes the TCP handshake without an accept loop).
        let listener_a = TcpListener::bind("127.0.0.1:0").expect("bind a");
        let addr_a = listener_a.local_addr().expect("addr a").to_string();
        let listener_b = TcpListener::bind("127.0.0.1:0").expect("bind b");
        let addr_b = listener_b.local_addr().expect("addr b").to_string();
        let listener_c = TcpListener::bind("127.0.0.1:0").expect("bind c");
        let addr_c = listener_c.local_addr().expect("addr c").to_string();

        let channel = crate::outbound::outbound_channel();
        let control = channel.control();
        let connected = crate::runtime::ConnectedPeers::new();
        let connector = crate::outbound::start_outbound(
            crate::outbound::OutboundConfig {
                template: control_template(),
                connected: connected.clone(),
                server: None,
                // Short enough that a leaked redial would be observed
                // by the post-remove stability check below.
                // Two normal-connection permits: the persistent peer
                // is exempt (dcrd 2.2), so two temporary connections
                // fill the table.
                manager: test_conn_manager_with_cap(50_000_000, 2),
                dial_timeout: std::time::Duration::from_secs(5),
                dialer: crate::socks::NodeDialer::direct(),
                persistent: Vec::new(),
                get_new_address: None,
                addr_manager: None,
            },
            channel,
        );

        let sync_peers = crate::dispatch::SyncPeers::new();
        let mut manager = NodeRpcConnManager::new(
            connected.clone(),
            Arc::new(crate::transport::NetByteTotals::new()),
        )
        .with_peer_registry(sync_peers.clone())
        .with_outbound(control);

        // addnode add: the dial succeeds and the served peer registers.
        manager.connect(&addr_a, true).expect("connect permanent");
        assert!(
            wait_until(std::time::Duration::from_secs(5), || connected.len() == 1),
            "the dialed peer must be served"
        );

        // A duplicate of a permanent request is refused up front with
        // the connection manager's raw error (dcrd 2.2 surfaces the
        // typed gate errors through the RPC adapter).
        assert_eq!(
            manager.connect(&addr_a, true),
            Err(format!(
                "a persistent connection for {addr_a} already exists"
            ))
        );
        // An unparseable address surfaces the resolution error.
        assert!(
            manager
                .connect("127.0.0.1", false)
                .expect_err("no port must fail")
                .contains("missing port"),
        );

        // node connect (temp) to the second listener fills the table.
        manager.connect(&addr_b, false).expect("connect temp");
        assert!(
            wait_until(std::time::Duration::from_secs(5), || connected.len() == 2),
            "the second dialed peer must be served"
        );
        // A duplicate of an established temporary request is refused...
        assert_eq!(
            manager.connect(&addr_b, false),
            Err(format!("a connection to {addr_b} is already established"))
        );
        // A third temporary connection uses the last permit...
        manager.connect(&addr_c, false).expect("connect third");
        assert!(
            wait_until(std::time::Duration::from_secs(5), || connected.len() == 3),
            "the third dialed peer must be served"
        );
        // ...and a fresh address is refused once the normal-connection
        // permits are used (the persistent peer does not count).
        assert_eq!(
            manager.connect("127.0.0.1:1", false),
            Err("a maximum of 2 connections is allowed".to_string())
        );

        // Register the permanent peer as its handshake would, carrying
        // the driver's first request id (a fresh manager assigns 1).
        let reg_listener = TcpListener::bind("127.0.0.1:0").expect("bind reg");
        let _client =
            TcpStream::connect(reg_listener.local_addr().expect("addr")).expect("connect");
        let (reg_socket, _) = reg_listener.accept().expect("accept");
        let reg_remote = reg_socket.peer_addr().expect("peer addr").to_string();
        let (queue, _rx) = crate::peerloop::OutboundQueue::channel();
        sync_peers.register(
            1,
            queue,
            Some(reg_socket),
            Arc::new(Mutex::new(crate::dispatch::RelayPeerState::new(
                crate::server::RelayPeerFacts {
                    connected: true,
                    services: dcroxide_wire::ServiceFlag::NODE_NETWORK,
                    wants_headers: false,
                    disable_relay_tx: false,
                    protocol_version: dcroxide_wire::PROTOCOL_VERSION,
                },
            ))),
            Arc::new(Mutex::new(dcroxide_peer::Peer::new_inbound(
                dcroxide_peer::Config::default(),
            ))),
            None,
            true,
            Some(1),
            Some(reg_remote.clone()),
            None,
        );

        // node remove: the request is removed so the permanent
        // connection is never redialed — the count settles at one (the
        // temporary peer) and stays there across several would-be retry
        // windows.  The dialed peer sits mid-handshake (the listener
        // never accepts), and Windows does not abort another thread's
        // in-flight blocking read when the local socket is shut down,
        // so the teardown is completed from the remote side instead:
        // accepting the backlog connection and dropping it delivers a
        // wire-level close every platform observes, while the listener
        // stays bound so a leaked redial of the removed request would
        // still reconnect and trip the stability check below.
        manager.remove_by_addr(&reg_remote).expect("remove");
        let (conn_a, _) = listener_a.accept().expect("accept the dialed conn");
        drop(conn_a);
        assert!(
            wait_until(std::time::Duration::from_secs(5), || connected.len() == 2),
            "the removed peer must disconnect"
        );
        std::thread::sleep(std::time::Duration::from_millis(300));
        assert_eq!(connected.len(), 2, "a removed peer is not redialed");

        // Removing an unknown or temporary peer is "peer not found".
        assert_eq!(manager.remove_by_id(99), Err("peer not found".to_string()));

        connector.shutdown();
        connected.disconnect_all();
    }

    /// `node remove` on an added peer that never connected falls back
    /// to cancelling its pending connection request (dcrd
    /// `RemoveByAddr` calling `CancelPending` after `removeNode` misses),
    /// and a second remove reports nothing pending.
    #[test]
    fn remove_by_addr_cancels_a_pending_connection() {
        // A port that refuses connections: bind, take the address, drop.
        let dead = std::net::TcpListener::bind("127.0.0.1:0").expect("bind");
        let dead_addr = dead.local_addr().expect("addr").to_string();
        drop(dead);

        let channel = crate::outbound::outbound_channel();
        let control = channel.control();
        let connected = crate::runtime::ConnectedPeers::new();
        let connector = crate::outbound::start_outbound(
            crate::outbound::OutboundConfig {
                template: control_template(),
                connected: connected.clone(),
                server: None,
                // Long enough that the scheduled retry never fires
                // mid-test, leaving the failed request pending.
                manager: test_conn_manager(60_000_000_000),
                dial_timeout: std::time::Duration::from_secs(5),
                dialer: crate::socks::NodeDialer::direct(),
                persistent: Vec::new(),
                get_new_address: None,
                addr_manager: None,
            },
            channel,
        );

        let mut manager =
            NodeRpcConnManager::new(connected, Arc::new(crate::transport::NetByteTotals::new()))
                .with_outbound(control);

        // The add succeeds (the checks pass); the dial then fails and
        // the permanent request waits for its retry.
        // A temporary connect to the dead port surfaces the dial
        // error synchronously (dcrd's Connect blocks the RPC through
        // the dial and returns its error).
        let err = manager
            .connect(&dead_addr, false)
            .expect_err("temp dial to a dead port fails");
        assert!(err.contains("dial failed"), "unexpected error: {err}");

        manager.connect(&dead_addr, true).expect("connect");
        // Re-adding the same peer is refused while its entry lives on,
        // with the connection manager's raw error (dcrd 2.2).
        assert_eq!(
            manager.connect(&dead_addr, true),
            Err(format!(
                "a persistent connection for {dead_addr} already exists"
            ))
        );

        // No connected persistent peer matches, so the remove falls back
        // to the connection manager's persistent entry; a second remove
        // finds nothing (dcrd 2.2's errPeerNotFound).
        manager.remove_by_addr(&dead_addr).expect("remove entry");
        assert_eq!(
            manager.remove_by_addr(&dead_addr),
            Err("peer not found".to_string())
        );

        connector.shutdown();
    }

    /// The handshake watchdog closes a dribbled connection at the
    /// deadline, and disarming stands it down.
    #[test]
    fn handshake_watchdog_fires_and_disarms() {
        use std::io::Read;
        use std::net::{TcpListener, TcpStream};

        // Fired: a reader with no timeout of its own is unblocked when
        // the watchdog shuts the socket down at the deadline.
        let listener = TcpListener::bind("127.0.0.1:0").expect("bind");
        let addr = listener.local_addr().expect("addr");
        let client = TcpStream::connect(addr).expect("connect");
        let (server_sock, _) = listener.accept().expect("accept");
        let now = Instant::now();
        let watchdog = arm_handshake_watchdog(
            server_sock.try_clone().ok(),
            now.checked_add(Duration::from_millis(150))
                .expect("deadline"),
        );
        // On Windows a local shutdown does not abort a blocked recv;
        // the socket timeout is the cross-platform fallback bounding
        // the test, while unix unblocks at the watchdog's deadline.
        server_sock
            .set_read_timeout(Some(Duration::from_secs(3)))
            .expect("read timeout");
        let mut reader = server_sock;
        let started = Instant::now();
        let mut byte = [0u8; 1];
        let outcome = reader.read(&mut byte);
        assert!(
            matches!(outcome, Ok(0) | Err(_)),
            "the watchdog must end the blocked read"
        );
        assert!(
            started.elapsed() < Duration::from_secs(5),
            "fired late: {:?}",
            started.elapsed()
        );
        drop(watchdog);
        drop(client);

        // Disarmed: the socket outlives the deadline.
        let listener = TcpListener::bind("127.0.0.1:0").expect("bind");
        let addr = listener.local_addr().expect("addr");
        let mut client = TcpStream::connect(addr).expect("connect");
        let (server_sock, _) = listener.accept().expect("accept");
        let now = Instant::now();
        let watchdog = arm_handshake_watchdog(
            server_sock.try_clone().ok(),
            now.checked_add(Duration::from_millis(100))
                .expect("deadline"),
        );
        watchdog.disarm();
        std::thread::sleep(Duration::from_millis(250));
        use std::io::Write;
        let mut server_sock = server_sock;
        server_sock
            .write_all(b"ok")
            .expect("a disarmed socket stays open");
        let mut buf = [0u8; 2];
        client.read_exact(&mut buf).expect("read");
        assert_eq!(&buf, b"ok");
    }
