/// The connection-manager adapter answering the RPC handlers' peer
/// queries over the daemon's live-peer registry (a growing slice of
/// the `RpcConnManager` seam).
pub struct NodeRpcConnManager {
    connected: crate::runtime::ConnectedPeers,
    net_totals: Arc<crate::transport::NetByteTotals>,
    /// The sync-manager peer registry, backing `getpeerinfo` (and the
    /// relay fan-out).  Held at the top level, not inside the relay
    /// option, so peer info is available even when the relay handles are
    /// not attached.
    sync_peers: crate::dispatch::SyncPeers,
    relay: Option<RpcRelaySinks>,
    /// The outbound connection driver's control handle, backing the
    /// manual connect/remove RPCs (dcrd's `rpcConnManager` reaching
    /// `cm.server.connManager`); absent only in tests without a driver.
    outbound: Option<crate::outbound::OutboundControl>,
    /// The dial/lookup routing (dcrd's `dcrdLookup` behind
    /// `rpcConnManager.Lookup`); direct unless configured otherwise.
    dialer: crate::socks::NodeDialer,
}

/// The relay handles the RPC connection manager drives when a
/// submitted transaction should announce to the network.
struct RpcRelaySinks {
    recently_advertised: Arc<Mutex<dcroxide_containers::lru::Map<Hash, dcroxide_wire::MsgTx>>>,
    tx_pool: Arc<Mutex<crate::txmempool::NodeTxPool>>,
    rebroadcast: crate::rebroadcast::RebroadcastSink,
    /// The websocket notification manager, so a submitted transaction
    /// reaches subscribed clients as well as peers (dcrd
    /// `handleSendRawTransaction` calling `NotifyNewTransactions`
    /// alongside `RelayTransactions`).
    ntfn: crate::websocket::NodeNtfnMgr,
}

impl NodeRpcConnManager {
    /// Adapt the connected-peer registry and byte totals for the RPC
    /// handlers.
    pub fn new(
        connected: crate::runtime::ConnectedPeers,
        net_totals: Arc<crate::transport::NetByteTotals>,
    ) -> NodeRpcConnManager {
        NodeRpcConnManager {
            connected,
            net_totals,
            sync_peers: crate::dispatch::SyncPeers::default(),
            relay: None,
            outbound: None,
            dialer: crate::socks::NodeDialer::direct(),
        }
    }

    /// Attach the configured dial/lookup routing so `getaddednodeinfo`'s
    /// DNS detail resolves the way dcrd's `dcrdLookup` does.
    pub fn with_dialer(mut self, dialer: crate::socks::NodeDialer) -> NodeRpcConnManager {
        self.dialer = dialer;
        self
    }

    /// Attach the outbound connection driver's control handle so the
    /// manual peer-control RPCs (`addnode`, `node connect`/`remove`) can
    /// dial and remove connection requests.
    pub fn with_outbound(
        mut self,
        outbound: crate::outbound::OutboundControl,
    ) -> NodeRpcConnManager {
        self.outbound = Some(outbound);
        self
    }

    /// Stop a removed persistent peer's redial by removing its
    /// connection request (dcrd's `removeNode` nilling the peer's
    /// `connReq` and calling `connManager.Remove`) — the shared tail of
    /// both remove seams.  A peer registered without a request id (or
    /// without a driver attached) has no redial to stop.
    fn stop_redial(&self, conn_req_id: Option<u64>) {
        if let (Some(outbound), Some(req_id)) = (&self.outbound, conn_req_id) {
            outbound.remove(req_id);
        }
    }

    /// Attach only the sync-manager peer registry that backs
    /// `getpeerinfo`, without the transaction-relay handles.  The daemon
    /// always installs both together through
    /// [`with_relay`](Self::with_relay), so this exists solely to build
    /// the peer-info seam in isolation for tests.
    #[cfg(test)]
    fn with_peer_registry(mut self, sync_peers: crate::dispatch::SyncPeers) -> NodeRpcConnManager {
        self.sync_peers = sync_peers;
        self
    }

    /// Attach the sync-manager peer registry (backing `getpeerinfo`) and
    /// the relay handles so submitted transactions announce to the
    /// network (dcrd rpcConnManager's `RelayTransactions`).
    pub fn with_relay(
        mut self,
        sync_peers: crate::dispatch::SyncPeers,
        recently_advertised: Arc<Mutex<dcroxide_containers::lru::Map<Hash, dcroxide_wire::MsgTx>>>,
        tx_pool: Arc<Mutex<crate::txmempool::NodeTxPool>>,
        rebroadcast: crate::rebroadcast::RebroadcastSink,
        ntfn: crate::websocket::NodeNtfnMgr,
    ) -> NodeRpcConnManager {
        self.sync_peers = sync_peers;
        self.relay = Some(RpcRelaySinks {
            recently_advertised,
            tx_pool,
            rebroadcast,
            ntfn,
        });
        self
    }
}

impl dcroxide_rpc::server::RpcConnManager for NodeRpcConnManager {
    fn connected_count(&mut self) -> i32 {
        self.connected.len() as i32
    }

    /// Snapshot the live peers for `getpeerinfo` (dcrd
    /// `rpcConnManager.ConnectedPeers`); the handler sorts by id and
    /// flags the sync peer itself.
    fn connected_peers(&mut self) -> Vec<dcroxide_rpc::server::RpcPeerInfo> {
        self.sync_peers.connected_peer_infos()
    }

    /// The persistent peers for `getaddednodeinfo` (dcrd
    /// `rpcConnManager.PersistentPeers`).
    fn persistent_peers(&mut self) -> Vec<dcroxide_rpc::server::RpcAddedNode> {
        self.sync_peers.persistent_peers()
    }

    /// Disconnect the non-permanent peer with the given id (dcrd
    /// `rpcConnManager.DisconnectByID`); a permanent or absent peer is
    /// "peer not found", which the `node` handler turns into its "use
    /// remove" hint.
    fn disconnect_by_id(&mut self, id: i32) -> Result<(), String> {
        if self.sync_peers.disconnect_by_id(id) {
            Ok(())
        } else {
            Err("peer not found".to_string())
        }
    }

    /// Disconnect the non-permanent peers at the given address (dcrd
    /// `rpcConnManager.DisconnectByAddr`).
    fn disconnect_by_addr(&mut self, addr: &str) -> Result<(), String> {
        if self.sync_peers.disconnect_by_addr(addr) {
            Ok(())
        } else {
            Err("peer not found".to_string())
        }
    }

    /// Add the address as a new persistent or one-try outbound peer
    /// (dcrd `rpcConnManager.Connect`): the driver's event loop runs the
    /// duplicate, resolution, and max-peers checks and dials after
    /// replying.
    fn connect(&mut self, addr: &str, permanent: bool) -> Result<(), String> {
        let outbound = self
            .outbound
            .as_ref()
            .ok_or_else(|| crate::outbound::STOPPED.to_string())?;
        outbound.connect(addr, permanent)
    }

    /// Remove the persistent peer with the given id (dcrd
    /// `rpcConnManager.RemoveByID` over `removeNode`): the peer is
    /// disconnected and its connection request removed so it is not
    /// redialed.  A temporary or unknown peer is "peer not found", which
    /// the `node` handler turns into its "use disconnect" hint when the
    /// peer is connected.
    fn remove_by_id(&mut self, id: i32) -> Result<(), String> {
        match self.sync_peers.remove_persistent_by_id(id) {
            Some(conn_req_id) => {
                self.stop_redial(conn_req_id);
                Ok(())
            }
            None => {
                // dcrd 2.2's fallback: treat the ID as a connection
                // manager ID and remove it when it is a persistent
                // entry (an added peer that never connected).
                if id >= 0
                    && let Some(outbound) = self.outbound.as_ref()
                    && outbound.remove_if_persistent(id as u64)
                {
                    return Ok(());
                }
                Err("peer not found".to_string())
            }
        }
    }

    /// Remove the persistent peer with the given address (dcrd
    /// `rpcConnManager.RemoveByAddr`): when no connected persistent peer
    /// matches, fall back to cancelling a pending connection request for
    /// the resolved address — an added peer that is still dialing or
    /// awaiting a retry.
    fn remove_by_addr(&mut self, addr: &str) -> Result<(), String> {
        match self.sync_peers.remove_persistent_by_addr(addr) {
            Some(conn_req_id) => {
                self.stop_redial(conn_req_id);
                Ok(())
            }
            None => {
                let outbound = self
                    .outbound
                    .as_ref()
                    .ok_or_else(|| crate::outbound::STOPPED.to_string())?;
                // dcrd 2.2's fallback: find the persistent entry for
                // the address in the connection manager and remove it
                // (an added peer still dialing or awaiting a retry).
                outbound.remove_persistent_by_addr(addr)
            }
        }
    }

    /// Resolve a host to its addresses for `getaddednodeinfo` with DNS
    /// details (dcrd `rpcConnManager.Lookup` over the config's lookup
    /// function; the system resolver here — a `.onion`/proxied host
    /// resolves through it rather than dcrd's SOCKS lookup, a documented
    /// divergence until the Tor path is wired).
    fn lookup(&mut self, host: &str) -> Result<Vec<String>, String> {
        // dcrd `rpcConnManager.Lookup` runs the configured `dcrdLookup`,
        // so a proxied daemon resolves through Tor and a `.onion` host
        // takes the onion route (one minute bounds the resolution like
        // the seeder's transport timeout; Go's resolver context here is
        // unbounded).
        self.dialer
            .lookup(host, std::time::Duration::from_secs(60))
            .map(|ips| ips.iter().map(|ip| ip.to_string()).collect())
    }

    /// Announce submitted transactions to the network (dcrd
    /// `relayTransactions` from the RPC adaptor).
    fn relay_transactions(&mut self, tx_hashes: &[Hash]) {
        let Some(relay) = &self.relay else {
            return;
        };
        let mut notify_pairs: Vec<(dcroxide_wire::MsgTx, i8)> = Vec::new();
        for hash in tx_hashes {
            let fetched = {
                let pool = relay.tx_pool.lock().expect("tx pool mutex poisoned");
                pool.fetch_transaction(hash)
            };
            let advertised = self
                .sync_peers
                .relay_inventory(&crate::server::RelayInvFacts {
                    inv_type: dcroxide_wire::InvType::TX,
                    inv_hash: *hash,
                    req_services: dcroxide_wire::ServiceFlag(0),
                    immediate: false,
                    data_is_block_header: false,
                    data_is_tx: true,
                });
            if let Some(tx) = fetched {
                let tree =
                    if dcroxide_stake::determine_tx_type(&tx) == dcroxide_stake::TxType::Regular {
                        dcroxide_wire::TX_TREE_REGULAR
                    } else {
                        dcroxide_wire::TX_TREE_STAKE
                    };
                // Cache as recently advertised only when a peer qualified
                // for the relay, matching dcrd's per-peer cache update.
                if advertised {
                    relay
                        .recently_advertised
                        .lock()
                        .expect("recently advertised poisoned")
                        .put(*hash, tx.clone());
                }
                notify_pairs.push((tx, tree));
            }
        }
        // Notify subscribed websocket clients of the accepted transactions,
        // mirroring dcrd's `handleSendRawTransaction` calling
        // `NotifyNewTransactions(acceptedTxs)` right after
        // `RelayTransactions`.  This seam's sole caller is that handler, so
        // bundling the notify here is observably equivalent.
        if !notify_pairs.is_empty() {
            relay.ntfn.notify_new_transactions(notify_pairs);
        }
    }

    fn add_rebroadcast_inventory(&mut self, tx_hash: &Hash, tx: &dcroxide_wire::MsgTx) {
        // Track user-submitted transactions for periodic rebroadcast
        // until they make it into a block (dcrd rpcConnManager
        // forwarding to the server's `AddRebroadcastInventory`).
        let Some(relay) = &self.relay else {
            return;
        };
        relay.rebroadcast.add_rebroadcast_inventory(tx_hash, tx);
    }

    fn net_totals(&mut self) -> (u64, u64) {
        // Received first, sent second (dcrd `NetTotals`).
        (
            self.net_totals
                .bytes_received
                .load(std::sync::atomic::Ordering::Relaxed),
            self.net_totals
                .bytes_sent
                .load(std::sync::atomic::Ordering::Relaxed),
        )
    }
}

/// The sync-manager adapter answering the RPC handlers' sync-state
/// queries over the shared sync manager (a growing slice of the
/// `RpcSyncManager` seam).
pub struct NodeRpcSyncManager {
    sync_manager: Arc<Mutex<crate::sync::NodeSyncManager>>,
    tx_pool: Arc<Mutex<crate::txmempool::NodeTxPool>>,
}

impl NodeRpcSyncManager {
    /// Adapt the sync manager and the transaction pool for the RPC
    /// handlers (dcrd's rpcSyncMgr holds both the sync manager and
    /// the pool).
    pub fn new(
        sync_manager: Arc<Mutex<crate::sync::NodeSyncManager>>,
        tx_pool: Arc<Mutex<crate::txmempool::NodeTxPool>>,
    ) -> NodeRpcSyncManager {
        NodeRpcSyncManager {
            sync_manager,
            tx_pool,
        }
    }
}

impl dcroxide_rpc::server::RpcSyncManager for NodeRpcSyncManager {
    fn sync_height(&mut self) -> i64 {
        self.sync_manager
            .lock()
            .expect("sync manager poisoned")
            .sync_height()
    }

    fn sync_peer_id(&mut self) -> i32 {
        self.sync_manager
            .lock()
            .expect("sync manager poisoned")
            .sync_peer_id()
    }

    /// Submit a transaction to the pool (dcrd rpcSyncMgr's
    /// `ProcessTransaction`, which reaches the pool directly).
    fn process_transaction(
        &mut self,
        tx: &dcroxide_wire::MsgTx,
        allow_orphan: bool,
        allow_high_fees: bool,
        tag: u64,
    ) -> Result<Vec<Hash>, dcroxide_rpc::server::SendTxFailure> {
        self.tx_pool
            .lock()
            .expect("tx pool mutex poisoned")
            .process_transaction(tx, allow_orphan, allow_high_fees, tag)
            .map_err(|err| dcroxide_rpc::server::SendTxFailure {
                is_rule_error: matches!(err, dcroxide_mempool::PoolError::Rule(_)),
                is_duplicate: crate::txmempool::is_duplicate_pool_error(&err),
                message: match &err {
                    dcroxide_mempool::PoolError::Rule(rule) => rule.description.clone(),
                    dcroxide_mempool::PoolError::Other(text) => text.clone(),
                },
            })
    }

    /// Whether the transaction confirmed in a recent block (dcrd
    /// `RecentlyConfirmedTxn` over the netsync APBF filter).
    fn recently_confirmed_txn(&mut self, hash: &Hash) -> bool {
        // Take the shared filter handle so the check does not hold
        // the sync manager across the read.
        let filter = self
            .sync_manager
            .lock()
            .expect("sync manager poisoned")
            .recently_confirmed_txns();
        let filter = filter.lock().expect("recently confirmed filter poisoned");
        filter.contains(&hash.0)
    }

    /// Process a locally-submitted block (from `submitblock` or the
    /// getwork solution branch) through the same path as blocks
    /// received from peers, relaying it and pruning the mempool on
    /// acceptance (dcrd rpcSyncMgr's `SubmitBlock`, which calls
    /// `syncManager.ProcessBlock`; the relay and prune run inside the
    /// manager's block processing exactly as for a network block).
    fn submit_block(
        &mut self,
        block: &MsgBlock,
    ) -> Result<(), dcroxide_rpc::server::SubmitBlockFailure> {
        self.sync_manager
            .lock()
            .expect("sync manager poisoned")
            .process_block(block)
            .map_err(|err| dcroxide_rpc::server::SubmitBlockFailure {
                // Every block-processing failure here classifies as a
                // rule error: the ported `Chain::process_block` only
                // ever surfaces `RuleError`s (database and assertion
                // faults panic per the project's decision-core
                // convention), and dcrd's `ErrDuplicateBlock` is itself
                // a `blockchain.RuleError`, so dcrd's non-rule
                // internal-error branch (the getwork-submission split)
                // is unreachable and `submitblock` never wrongly reports
                // an internal error where dcrd would `rejected: ...`.
                // The message renders every block-processing error
                // exactly as dcrd's `blockchain.MultiError.Error`
                // combines them: a lone error unadorned (the common
                // single-error rejection) and two or more as dcrd's
                // `multiple errors (N):` block.  dcrd's `ProcessBlock`
                // flattens the reorganization multi error into the
                // acceptance error rather than nesting it, so the flat
                // error slice `NodeSyncChain::process_block` returns
                // renders byte-for-byte identically for the rare block
                // that both fails acceptance and then errors reorganizing.
                is_rule_error: true,
                message: err.message,
            })
    }
}

